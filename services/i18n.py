def t(key: str, language: str = "it", **kwargs) -> str:
    """
    Translate a key to the specified language
    
    Args:
        key: Translation key
        language: Language code (it/en/es/fr/de)
        **kwargs: Variables to interpolate in the translation
    
    Returns:
        Translated string with interpolated variables
    """
    # Get translation dict for language, fallback to Italian
    lang_dict = TRANSLATIONS.get(language, TRANSLATIONS["it"])
    
    # Get translation, fallback to key itself
    translation = lang_dict.get(key, key)
    
    # Interpolate variables if provided
    if kwargs:
        try:
            translation = translation.format(**kwargs)
        except KeyError:
            # If interpolation fails, return as-is
            pass
    
    return translation

def get_ui_strings(language: str = "it") -> dict:
    """
    Get all UI strings for a language (for frontend)
    
    Args:
        language: Language code
    
    Returns:
        Dictionary of all UI strings
    """
    return TRANSLATIONS.get(language, TRANSLATIONS["it"])
